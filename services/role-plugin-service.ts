/**
 * Unified persona creation — used by both the wizard (predefined role plugins)
 * and Haephestos (custom TOML-generated plugins).
 *
 * Exactly one of `tomlContent` or `pluginName` must be provided:
 * - tomlContent: Haephestos flow — generates a new plugin, adds to local marketplace
 * - pluginName: Wizard flow — uses an existing predefined role plugin
 *
 * In both cases, creates ~/agents/<personaName>/ and installs the plugin
 * with --scope local into that folder.
 */
export async function createPersona(opts: {
  personaName: string
  tomlContent?: string
  pluginName?: string
  marketplaceName?: string
  agentDescription?: string
}): Promise<CreatePersonaResult> {
  const { personaName, tomlContent, agentDescription } = opts

  // Validate persona name: lowercase alphanumeric + hyphens
  if (!/^[a-z0-9]([a-z0-9-]*[a-z0-9])?$/.test(personaName)) {
    throw new Error('personaName must be lowercase alphanumeric with hyphens (e.g. "peter-bot")')
  }

  // Must provide exactly one source
  if (tomlContent && opts.pluginName) {
    throw new Error('Provide either tomlContent or pluginName, not both')
  }
  if (!tomlContent && !opts.pluginName) {
    throw new Error('Either tomlContent or pluginName is required')
  }

  let pluginName: string
  let mainAgentName: string
  let marketplaceName: string

  if (tomlContent) {
    // Haephestos flow: generate plugin from TOML → local marketplace
    // Note: Haephestos is responsible for adding compatible-titles/clients to the TOML
    // before calling this API. The API saves the TOML as-is.
    const result = await generatePluginFromToml(tomlContent, agentDescription)
    pluginName = result.pluginName
    mainAgentName = result.mainAgentName
    marketplaceName = LOCAL_MARKETPLACE_NAME
  } else {
    // Wizard flow: use existing predefined plugin
    pluginName = opts.pluginName!
    const predefined = PREDEFINED_ROLE_PLUGINS[pluginName]
    if (predefined) {
      marketplaceName = opts.marketplaceName || predefined.marketplace
      mainAgentName = predefined.mainAgent
    } else {
      // Custom plugin name — caller must specify marketplace
      marketplaceName = opts.marketplaceName || LOCAL_MARKETPLACE_NAME
      mainAgentName = `${pluginName}-main-agent`
    }
  }

  // Create the persona folder: ~/agents/<personaName>/
  const agentDir = join(HOME, 'agents', personaName)
  await mkdir(agentDir, { recursive: true })

  // Install the plugin locally into the persona folder
  await _installPluginLocally(pluginName, agentDir, marketplaceName)

  return {
    personaName,
    agentDir,
    pluginName,
    mainAgentName,
    marketplaceName,
  }
}

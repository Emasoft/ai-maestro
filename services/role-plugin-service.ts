/**
 * List all available role plugins, merging two sources:
 * 1. GitHub marketplace manifest — reads marketplace.json to discover available role-plugins
 *    (these are NOT installed yet — they're available for on-demand installation)
 * 2. Local marketplace at ~/agents/role-plugins/ — custom Haephestos-generated plugins
 *
 * Deduplicates by name — marketplace plugins take priority over local ones.
 * NOTE: ai-maestro is NOT a role-plugin despite having the ai-maestro- prefix.
 * Role-plugin identification requires the full quad-match (see CLAUDE.md).
 */
// Governance titles compatible with each predefined role-plugin (from ecosystem-constants)
const PREDEFINED_COMPATIBLE_TITLES = PLUGIN_COMPATIBLE_TITLES

export async function listRolePlugins(): Promise<RolePlugin[]> {
  const pluginsByName = new Map<string, RolePlugin>()

  // Source 1: Predefined role-plugins — always included as the authoritative defaults.
  // These are installed on-demand via `claude install <name>@ai-maestro-plugins --scope local`.
  // PREDEFINED_ROLE_PLUGINS is the source of truth, not the marketplace manifest category field.
  const predefinedDescriptions: Record<string, string> = {
    'ai-maestro-assistant-manager-agent': 'MANAGER — user interlocutor, directs other roles',
    'ai-maestro-chief-of-staff': 'CHIEF-OF-STAFF — per-team agent management, staff planning',
    'ai-maestro-architect-agent': 'ARCHITECT — design documents, requirements, architecture',
    'ai-maestro-orchestrator-agent': 'ORCHESTRATOR — task distribution, kanban, coordination',
    'ai-maestro-integrator-agent': 'INTEGRATOR — quality gates, PR review, merging, releases',
    'ai-maestro-programmer-agent': 'PROGRAMMER — general-purpose implementer, writes code',
    'ai-maestro-maintainer-agent': 'MAINTAINER — single-repo maintenance, issue triage, webhook-driven bugfixes',
  }
  for (const [name, info] of Object.entries(PREDEFINED_ROLE_PLUGINS)) {
    pluginsByName.set(name, {
      name,
      version: '1.0.0',
      description: predefinedDescriptions[name] || '',
      model: undefined,
      program: undefined,
      pluginDir: join(GITHUB_MARKETPLACE_DIR, 'plugins', name),
      source: 'marketplace',
      marketplace: info.marketplace,
      compatibleTitles: PREDEFINED_COMPATIBLE_TITLES[name],
      compatibleClients: ['claude-code'], // All 7 predefined role-plugins are Claude Code exclusive
    })
  }

  // Source 2: Local marketplace — custom Haephestos-generated plugins at ~/agents/role-plugins/
  if (existsSync(PLUGINS_DIR)) {
    const entries = await readdir(PLUGINS_DIR)
    for (const entry of entries) {
      // Skip if already found in marketplace (marketplace takes priority)
      if (pluginsByName.has(entry)) continue

      const pluginDir = join(PLUGINS_DIR, entry)
      const pluginJsonPath = join(pluginDir, '.claude-plugin', 'plugin.json')
      const tomlPath = join(pluginDir, `${entry}.agent.toml`)

      const entryStat = await stat(pluginDir).catch(() => null)
      if (!entryStat?.isDirectory()) continue
      if (!existsSync(pluginJsonPath)) continue

      try {
        const meta = JSON.parse(await readFile(pluginJsonPath, 'utf-8'))
        let model: string | undefined
        let program: string | undefined

        let compatibleTitles: string[] | undefined
        let compatibleClients: string[] | undefined
        if (existsSync(tomlPath)) {
          try {
            const tomlContent = await readFile(tomlPath, 'utf-8')
            const parsed = parseAgentToml(tomlContent)
            model = parsed.agent.model
            program = parsed.agent.program
            compatibleTitles = parsed.agent.compatibleTitles
            compatibleClients = parsed.agent.compatibleClients
          } catch { /* ignore parse errors */ }
        }

        pluginsByName.set(entry, {
          name: meta.name || entry,
          version: meta.version || '1.0.0',
          description: meta.description || '',
          model,
          program,
          pluginDir,
          source: 'local',
          marketplace: LOCAL_MARKETPLACE_NAME,
          compatibleTitles,
          compatibleClients,
        })
      } catch { /* skip malformed plugins */ }
    }
  }

  return Array.from(pluginsByName.values())
}

/**
 * Return role-plugins filtered by compatible-titles and optionally compatible-clients.
 * Both comparisons are case-insensitive.
 *
 * Client name normalization: callers may pass the short form (`claude`, `codex`)
 * OR the canonical form used in TOML files (`claude-code`, `codex`). We normalize
 * the short forms to the canonical TOML form before comparing, so the wizard
 * (which uses short form `claude`) matches predefined plugins (which declare
 * `compatible-clients = ["claude-code"]`). Without this normalization the filter
 * silently excludes ALL predefined plugins for any team title.
 */
export async function getPluginsForTitle(title: string, clientType?: string): Promise<RolePlugin[]> {
  const all = await listRolePlugins()
  const upperTitle = title.toUpperCase()
  // Normalize short → canonical form used in .agent.toml compatible-clients.
  const CLIENT_ALIAS_MAP: Record<string, string> = {
    'claude': 'claude-code',
    'claude-code': 'claude-code',
    'codex': 'codex',
    'gemini': 'gemini',
    'opencode': 'opencode',
    'kiro': 'kiro',
  }
  const rawClient = clientType?.toLowerCase()
  const canonicalClient = rawClient ? (CLIENT_ALIAS_MAP[rawClient] ?? rawClient) : undefined
  return all.filter(p => {
    // Must match compatible-titles
    if (!p.compatibleTitles?.includes(upperTitle)) return false
    // If client filter specified, must also match compatible-clients (or have none = any client)
    if (canonicalClient && p.compatibleClients && p.compatibleClients.length > 0) {
      if (!p.compatibleClients.includes(canonicalClient)) return false
    }
    return true
  })
}

/**
 * Delete a role plugin from the local marketplace.
 * Cannot delete default marketplace role plugins — those are managed by `claude plugin`.
 */
export async function deleteRolePlugin(pluginName: string): Promise<void> {
  if (!/^[a-zA-Z0-9][a-zA-Z0-9_-]*$/.test(pluginName)) {
    throw new Error(`Invalid plugin name "${pluginName}"`)
  }

  // Guard: default marketplace plugins cannot be deleted via this API
  if (DEFAULT_ROLE_PLUGIN_NAMES.includes(pluginName)) {
    throw new Error(`Cannot delete default marketplace role plugins. Use "claude plugin uninstall ${pluginName}" instead.`)
  }

  const pluginDir = join(PLUGINS_DIR, pluginName)
  if (existsSync(pluginDir)) {
    await rm(pluginDir, { recursive: true })
  }

  // Remove from marketplace manifest
  if (existsSync(MARKETPLACE_JSON)) {
    const manifest = await loadJsonSafe(MARKETPLACE_JSON) as Record<string, unknown>
    const plugins = (manifest.plugins || []) as Array<Record<string, string>>
    manifest.plugins = plugins.filter(p => p.name !== pluginName)
    await writeFile(MARKETPLACE_JSON, JSON.stringify(manifest, null, 2) + '\n')
  }

  // Remove from global enabledPlugins
  const pluginKey = `${pluginName}@${LOCAL_MARKETPLACE_NAME}`
  if (existsSync(USER_GLOBAL_SETTINGS)) {
    const settings = await loadJsonSafe(USER_GLOBAL_SETTINGS) as Record<string, Record<string, unknown>>
    const ep = (settings.enabledPlugins || {}) as Record<string, boolean>
    delete ep[pluginKey]
    settings.enabledPlugins = ep
    await saveJsonSafe(USER_GLOBAL_SETTINGS, settings)
  }
}


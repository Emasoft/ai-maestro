export interface SyncDefaultsResult {
  synced: string[]
  skipped: string[]
  errors: Array<{ name: string; error: string }>
  available: string[]
}

/**
 * Ensure the Emasoft/ai-maestro-plugins marketplace is registered and return
 * the list of available role-plugins from the marketplace metadata.
 *
 * IMPORTANT: This does NOT install any role-plugins. Role-plugins are installed
 * ON-DEMAND when a user selects one from the dropdown. This function only:
 * 1. Ensures the marketplace Emasoft/ai-maestro-plugins is registered
 * 2. Returns the list of available role-plugins from the marketplace manifest
 * 3. Migrates existing agent settings from old marketplace keys
 *
 * @param _force - Kept for API compatibility (ignored — nothing to force-install)
 */
export async function syncDefaultRolePlugins(_force = false): Promise<SyncDefaultsResult> {
  const synced: string[] = []
  const skipped: string[] = []
  const errors: Array<{ name: string; error: string }> = []
  const available: string[] = []

  // Step 1: Ensure the GitHub marketplace is registered (does not install any plugins)
  try {
    await execFileAsync('claude', ['plugin', 'marketplace', 'add', MARKETPLACE_REPO], { timeout: 60000 })
    synced.push('marketplace-registered')
    console.log(`[role-plugins] GitHub marketplace ${MARKETPLACE_REPO} registered`)
  } catch (err) {
    // Marketplace may already be registered — not a fatal error
    const msg = err instanceof Error ? err.message : String(err)
    if (msg.includes('already') || msg.includes('exists')) {
      skipped.push('marketplace-already-registered')
    } else {
      errors.push({ name: 'marketplace-registration', error: msg })
    }
    console.warn('[role-plugins] Marketplace add note:', msg)
  }

  // Step 2: Read the marketplace manifest, fix source format if needed, discover role-plugins.
  // Claude CLI requires source format: { "source": "url", "url": "<git_url>" }
  // but the upstream repo may use { "type": "git", "repo": "<git_url>" } which doesn't work.
  if (existsSync(GITHUB_MARKETPLACE_MANIFEST)) {
    try {
      const manifest = JSON.parse(await readFile(GITHUB_MARKETPLACE_MANIFEST, 'utf-8')) as {
        plugins?: Array<{ name: string; category?: string; source?: unknown }>
      }
      const plugins = manifest.plugins || []
      let manifestFixed = false
      for (const entry of plugins) {
        // Fix source format: { type: "git", repo: "..." } → { source: "url", url: "..." }
        const src = entry.source as Record<string, string> | string | undefined
        if (src && typeof src === 'object' && (src as Record<string, string>).type === 'git' && (src as Record<string, string>).repo) {
          ;(entry as Record<string, unknown>).source = { source: 'url', url: (src as Record<string, string>).repo }
          manifestFixed = true
        }
        // Collect available role-plugins
        if (entry.category === 'role-plugin') {
          available.push(entry.name)
        }
      }
      // Write back the fixed manifest so `claude plugin install` works
      if (manifestFixed) {
        await writeFile(GITHUB_MARKETPLACE_MANIFEST, JSON.stringify(manifest, null, 4) + '\n')
        console.log('[role-plugins] Fixed marketplace manifest source format for Claude CLI compatibility')
      }
    } catch {
      console.warn('[role-plugins] Could not read marketplace manifest for available plugins')
    }
  }

  // Also include the known predefined names (in case manifest hasn't been updated yet)
  for (const name of DEFAULT_ROLE_PLUGIN_NAMES) {
    if (!available.includes(name)) {
      available.push(name)
    }
  }

  // Step 3: Migrate existing agent settings from old local marketplace key to GitHub marketplace key
  await migrateDefaultPluginSettings()

  return { synced, skipped, errors, available }
}

/**
 * Migrate agent settings: change old marketplace keys to current @ai-maestro-plugins.
 * Also removes the deprecated 23blocks-OS marketplace from Claude CLI.
 */
async function migrateDefaultPluginSettings(): Promise<void> {
  // Step 1: Remove deprecated 23blocks-OS marketplace (replaced by Emasoft/ai-maestro-plugins)
  try {
    const { execSync } = await import('child_process')
    // Try both short and full URL forms — CLI may accept either
    execSync('claude plugin marketplace remove 23blocks-OS/ai-maestro-plugins', { timeout: 10000, stdio: 'pipe' }).toString()
    console.log('[role-plugins] Removed deprecated 23blocks-OS marketplace')
  } catch {
    try {
      const { execSync } = await import('child_process')
      execSync('claude plugin marketplace remove https://github.com/23blocks-OS/ai-maestro-plugins', { timeout: 10000, stdio: 'pipe' }).toString()
      console.log('[role-plugins] Removed deprecated 23blocks-OS marketplace (full URL)')
    } catch {
      // Not registered or already removed — fine
    }
  }

  // Step 1b: Remove deprecated local marketplace names from global settings
  try {
    const settings = await loadJsonSafe(USER_GLOBAL_SETTINGS) as Record<string, Record<string, unknown>>
    const ekm = (settings.extraKnownMarketplaces || {}) as Record<string, unknown>
    const deprecatedNames = ['ai-maestro-local-agents-marketplace', 'ai-maestro-local-marketplace', 'role-plugins']
    let cleaned = false
    for (const name of deprecatedNames) {
      if (ekm[name]) { delete ekm[name]; cleaned = true }
    }
    if (cleaned) {
      settings.extraKnownMarketplaces = ekm
      await saveJsonSafe(USER_GLOBAL_SETTINGS, settings)
      console.log('[role-plugins] Cleaned deprecated marketplace names from global settings')
    }
  } catch { /* ignore */ }

  // Step 2: Migrate agent settings from old local marketplace key to GitHub marketplace key
  const agentsBaseDir = join(HOME, 'agents')
  if (!existsSync(agentsBaseDir)) return

  try {
    const entries = await readdir(agentsBaseDir)
    for (const entry of entries) {
      const settingsPath = join(agentsBaseDir, entry, '.claude', 'settings.local.json')
      if (!existsSync(settingsPath)) continue

      try {
        const settings = await loadJsonSafe(settingsPath) as Record<string, Record<string, unknown>>
        const ep = (settings.enabledPlugins || {}) as Record<string, boolean>
        let changed = false

        for (const pluginName of DEFAULT_ROLE_PLUGIN_NAMES) {
          // Migrate from local marketplace key → GitHub marketplace key
          const oldLocalKey = `${pluginName}@${LOCAL_MARKETPLACE_NAME}`
          const newKey = `${pluginName}@${GITHUB_MARKETPLACE_NAME}`
          if (ep[oldLocalKey] !== undefined && ep[newKey] === undefined) {
            ep[newKey] = ep[oldLocalKey]
            delete ep[oldLocalKey]
            changed = true
          }
          // Also migrate from old deprecated names (ai-maestro-local-agents-marketplace, etc.)
          const legacyKeys = [
            `${pluginName}@ai-maestro-local-agents-marketplace`,
            `${pluginName}@ai-maestro-local-marketplace`,
            `${pluginName}@role-plugins`,
          ]
          for (const legacyKey of legacyKeys) {
            if (ep[legacyKey] !== undefined && ep[newKey] === undefined) {
              ep[newKey] = ep[legacyKey]
              delete ep[legacyKey]
              changed = true
            } else if (ep[legacyKey] !== undefined) {
              delete ep[legacyKey]  // Duplicate — just remove
              changed = true
            }
          }
        }

        if (changed) {
          settings.enabledPlugins = ep
          await saveJsonSafe(settingsPath, settings)
          console.log(`[role-plugins] Migrated settings for agent: ${entry}`)
        }
      } catch { /* skip individual agent errors */ }
    }
  } catch { /* skip if agents dir can't be read */ }
}


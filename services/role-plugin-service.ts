/**
 * Role-Plugin Service
 *
 * Converts .agent.toml profiles into Claude Code plugins, manages a local
 * marketplace at ~/agents/role-plugins/, and installs plugins into agent
 * working directories with --scope local.
 *
 * The local marketplace trick: Claude Code requires plugins to come from
 * registered marketplaces. We create a directory-based marketplace and
 * register it in settings.local.json via extraKnownMarketplaces. Each
 * agent then enables only its own role-plugin via a project-level
 * .claude/settings.local.json (equivalent to --scope local).
 *
 * IMPORTANT: This local marketplace mechanism is designed EXCLUSIVELY for
 * role-plugins generated by Haephestos or the AI Maestro API. It is NOT a
 * general-purpose plugin installation method. Regular plugins should always
 * be installed from GitHub-hosted marketplaces.
 *
 * GOVERNANCE → ROLE-PLUGIN BINDING RULES:
 *
 * Title MANAGER → MUST use ai-maestro-assistant-manager-agent (auto-installed)
 * Title CHIEF-OF-STAFF → MUST use ai-maestro-chief-of-staff (auto-installed)
 * Title MEMBER → can use any role-plugin (user choice)
 *
 * Auto-installation triggers:
 * 1. POST /api/governance/manager — assigns MANAGER → installs plugin
 * 2. POST /api/teams/{id}/chief-of-staff — assigns COS → installs plugin
 * 3. POST /api/teams (type=closed, chiefOfStaffId) — creates team + COS → installs plugin
 *
 * Role-plugins from marketplace: installed via `claude plugin install <name>@ai-maestro-plugins --scope local`
 *   Claude CLI handles cloning, caching (~/.claude/plugins/cache/), and settings.local.json
 * Custom role-plugins: installed from ~/agents/role-plugins/ local marketplace via settings.local.json
 *
 * Agents (MANAGER, COS) can create MEMBER agents and assign role-plugins via:
 *   aimaestro-agent.sh plugin install <agent> <plugin>@<marketplace> --scope local
 */


// ============================================================================

beforeEach(() => {
  fsStore = {}
  uuidCounter = 0
  vi.clearAllMocks()
})

// ============================================================================
// GET /api/teams/[id]/documents - List documents
// ============================================================================

describe('GET /api/teams/[id]/documents', () => {
  it('returns 404 when team does not exist', async () => {
    const req = makeRequest(`/api/teams/${MISSING_TEAM_ID}/documents`)
    const res = await listDocuments(req, makeParams(MISSING_TEAM_ID) as any)

    expect(res.status).toBe(404)
    const data = await res.json()
    expect(data.error).toBe('Team not found')
  })

  it('returns empty documents array for team with no docs', async () => {
    const team = await createTeam({ name: 'Test Team', agentIds: [] })

    const req = makeRequest(`/api/teams/${team.id}/documents`)
    const res = await listDocuments(req, makeParams(team.id) as any)

    expect(res.status).toBe(200)
    const data = await res.json()
    expect(data.documents).toEqual([])
  })

  it('returns documents for a team', async () => {
    const team = await createTeam({ name: 'Docs Team', agentIds: [] })
    await createDocument({ teamId: team.id, title: 'Doc 1', content: 'Content 1' })
    await createDocument({ teamId: team.id, title: 'Doc 2', content: 'Content 2' })

    const req = makeRequest(`/api/teams/${team.id}/documents`)
    const res = await listDocuments(req, makeParams(team.id) as any)

    expect(res.status).toBe(200)
    const data = await res.json()
    expect(data.documents).toHaveLength(2)
    expect(data.documents[0].title).toBe('Doc 1')
    expect(data.documents[1].title).toBe('Doc 2')
  })
})


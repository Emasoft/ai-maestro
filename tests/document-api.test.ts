describe('POST /api/teams/[id]/documents', () => {
  it('returns 404 when team does not exist', async () => {
    const req = makeRequest(`/api/teams/${MISSING_TEAM_ID}/documents`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ title: 'Test', content: 'body' }),
    })
    const res = await createDocumentRoute(req, makeParams(MISSING_TEAM_ID) as any)

    expect(res.status).toBe(404)
  })

  it('returns 400 when title is missing', async () => {
    const team = await createTeam({ name: 'Test Team', agentIds: [] })

    const req = makeRequest(`/api/teams/${team.id}/documents`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ content: 'body' }),
    })
    const res = await createDocumentRoute(req, makeParams(team.id) as any)

    expect(res.status).toBe(400)
    const data = await res.json()
    expect(data.error).toBe('title is required')
  })

  it('creates a document with 201 status', async () => {
    const team = await createTeam({ name: 'Create Team', agentIds: [] })

    const req = makeRequest(`/api/teams/${team.id}/documents`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ title: 'New Doc', content: 'Hello world', pinned: true, tags: ['api'] }),
    })
    const res = await createDocumentRoute(req, makeParams(team.id) as any)

    expect(res.status).toBe(201)
    const data = await res.json()
    expect(data.document.title).toBe('New Doc')
    expect(data.document.content).toBe('Hello world')
    expect(data.document.pinned).toBe(true)
    expect(data.document.tags).toEqual(['api'])
  })

  it('persists created document', async () => {
    const team = await createTeam({ name: 'Persist Team', agentIds: [] })

    const req = makeRequest(`/api/teams/${team.id}/documents`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ title: 'Persisted', content: 'data' }),
    })
    await createDocumentRoute(req, makeParams(team.id) as any)

    const docs = loadDocuments(team.id)
    expect(docs).toHaveLength(1)
    expect(docs[0].title).toBe('Persisted')
  })

  // SECURITY: Stored XSS risk -- API stores values verbatim; frontend MUST sanitize before rendering.
  // Phase 2: Add server-side HTML sanitization for document titles and content.
  it('stores HTML content verbatim without sanitization (known security surface)', async () => {
    const team = await createTeam({ name: 'XSS Team', agentIds: [] })
    const xssTitle = '<script>alert(1)</script>'

    const req = makeRequest(`/api/teams/${team.id}/documents`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ title: xssTitle, content: '<img onerror="alert(2)" src=x>' }),
    })
    const res = await createDocumentRoute(req, makeParams(team.id) as any)

    expect(res.status).toBe(201)
    const data = await res.json()
    // API stores values verbatim — this is a known stored-XSS security surface; frontend MUST sanitize before rendering
    expect(data.document.title).toBe(xssTitle)
    expect(data.document.content).toBe('<img onerror="alert(2)" src=x>')

    // Verify persistence also stores verbatim
    const docs = loadDocuments(team.id)
    expect(docs[0].title).toBe(xssTitle)
  })

  it('defaults content to empty string when not provided', async () => {
    const team = await createTeam({ name: 'Default Content', agentIds: [] })

    const req = makeRequest(`/api/teams/${team.id}/documents`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ title: 'No Content' }),
    })
    const res = await createDocumentRoute(req, makeParams(team.id) as any)

    expect(res.status).toBe(201)
    const data = await res.json()
    expect(data.document.content).toBe('')
  })
})

